/**
 * An example main function
 * @returns void
 */
function main(): void {
    addTwoNumbers(1, 2);
}

/**
 * Adds two numbers together
 * @param {number} num1 The first inputted number
 * @param {number} num2 The second inputted number
 * @returns number The sum of the num1 and num2 values
 */
function addTwoNumbers(num1: number, num2: number): number {
    return num1+num2;
}